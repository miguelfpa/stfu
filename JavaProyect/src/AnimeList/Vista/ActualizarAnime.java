    /**
     * Capturamos el evento al pulsar el boton actualizar 
     * Recogemos todos los campos en las variables y los mandamos al metodo updateAnime
     * al inicializar con una instancia de App
     * @param evt 
     */
    private void cambiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cambiarActionPerformed
        // TODO add your handling code here:
        App app = new App();
        
            String cad1 = tituloViejo.getText();
            String cad2 = (String) generos.getSelectedItem();
            int puntua = (Integer) puntuacion.getValue();
            String anime = tituloNuevo.getText();
            int capi = (Integer) capitulos.getValue();
        
            
            app.updateAnime(cad1, cad2, anime, puntua, capi);
            System.out.println(app.getAnimes());
    }//GEN-LAST:event_cambiarActionPerformed

